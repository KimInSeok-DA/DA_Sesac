-- 개선 전 SQL (SIMPLE_CASE_EXPRESSION)

SELECT DEPTNO,
       SUM(COALESCE((CASE JOB WHEN 'MANAGER' THEN 1 ELSE 0 END),0)) "MANAGER",
       SUM(COALESCE((CASE JOB WHEN 'ALALYST' THEN 1 ELSE 0 END),0)) "ALALYST",
       SUM(COALESCE((CASE JOB WHEN 'SALESMAN' THEN 1 ELSE 0 END),0)) "SALESMAN",
       SUM(COALESCE((CASE JOB WHEN 'CLERK' THEN 1 ELSE 0 END),0)) "CLERK"
FROM   EMP
GROUP  BY DEPTNO;

-- 개선 후 SQL (SIMPLE_CASE_EXPRESSION)

SELECT DEPTNO,
       COALESCE(SUM(CASE JOB WHEN 'MANAGER' THEN 1 END),0) "MANAGER",
       COALESCE(SUM(CASE JOB WHEN 'ALALYST' THEN 1 END),0) "ALALYST",
       COALESCE(SUM(CASE JOB WHEN 'SALESMAN' THEN 1 END),0) "SALESMAN",
       COALESCE(SUM(CASE JOB WHEN 'CLERK' THEN 1 END),0) "CLERK"
FROM   EMP
GROUP  BY DEPTNO;

