-- SIMPLE_CASE_EXPRESSION

SELECT LOC,
  CASE LOC
    WHEN 'NEW YORK' THEN 'EAST'
    WHEN 'BOSTON' THEN 'EAST'
    WHEN 'CHICAGO' THEN 'CENTER'
    WHEN 'DALLAS' THEN 'CENTER'
    ELSE 'ETC'
  END AS AREA
FROM DEPT;


-- SEARCHED_CASE_EXPRESSION

SELECT ENAME, 
  CASE WHEN SAL >= 2000 THEN 'HIGH'
       WHEN SAL >= 1000 THEN 'MID'
       ELSE 'LOW'
  END AS SAL_GRADE
FROM EMP;

-- CASE의 중첩

SELECT ENAME, SAL,
  CASE WHEN SAL >= 2000
       THEN 1500
       ELSE (CASE WHEN SAL >= 1000
                  THEN 1000
                  ELSE SAL * 0.5
             END)
  END AS BONUS
FROM EMP;

