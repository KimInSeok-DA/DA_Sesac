SELECT EMP.EMPNO, EMP.ENAME, ORG.DNAME
FROM   EMP EMP, DEPT ORG
WHERE  EMP.DEPTNO = ORG.DEPTNO;

--- Inner join
SELECT EMP.EMPNO, EMP.ENAME, DEPT.DNAME
FROM   EMP INNER JOIN DEPT
  ON   EMP.DEPTNO = DEPT.DEPTNO;

SELECT EMP.EMPNO, EMP.ENAME, ORG.DNAME
FROM   EMP EMP JOIN DEPT ORG
  ON   EMP.DEPTNO = ORG.DEPTNO;

SELECT E.EMPNO, D.DEPTNO, D.DNAME
FROM   EMP E INNER JOIN DEPT D 
  ON  (E.DEPTNO = D.DEPTNO);

SELECT E.ENAME, E.DEPTNO, O.DEPTNO, O.DNAME
FROM   EMP E JOIN DEPT O
  ON  (E.DEPTNO = O.DEPTNO)
WHERE  E.DEPTNO = '10';

--- Cross Join
SELECT E.ENAME, O.DNAME
FROM   EMP E, DEPT O
ORDER  BY ENAME;

SELECT E.ENAME, O.DNAME
FROM   EMP E CROSS JOIN DEPT O
ORDER  BY ENAME;

SELECT E.ENAME, O.DNAME
FROM   EMP E NATURAL JOIN DEPT O;

