SELECT E.EMPNO 사번, E.ENAME 사원명
     , E.JOB 직무, O.DNAME 부서명
FROM   EMP E LEFT OUTER JOIN DEPT O
       ON E.DEPTNO = O.DEPTNO
WHERE  E.JOB = 'CLERK';

SELECT E.EMPNO 사번, E.ENAME 사원명, E.JOB 직책, O.DNAME 부서명
