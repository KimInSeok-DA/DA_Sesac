---윈도우 함수
----순위 함수
SELECT MPG,
       COUNT(*),
       RANK() OVER(ORDER BY COUNT(*) DESC) AS RANK
FROM MTCARS
GROUP BY MPG;

-- 집계함수
SELECT NAME,
       CYL,
       COUNT(*) OVER(PARTITION BY CYL) AS PART_CYL_CNT
FROM MTCARS
WHERE CYL <= 6;

SELECT NAME,
       CYL,
       MPG,
       MAX(MPG) OVER(PARTITION BY CYL) AS PART_MAX_MPG
FROM MTCARS
WHERE CYL <= 6;

-- 행 순서 함수
SELECT NAME,
       CYL,
       MPG,
       FIRST_VALUE(MPG) OVER(PARTITION BY CYL) AS PART_FIRST_MPG
FROM MTCARS
WHERE CYL <= 6;

SELECT NAME,
       CYL,
       MPG,
       LAG(MPG, 2) OVER(ORDER BY MPG) AS MPG_2
FROM MTCARS
WHERE CYL <= 6;

-- 비율 함수
SELECT NAME,
       CYL,
       MPG,
       CUME_DIST() OVER(ORDER BY MPG) AS C_DIST,
       PERCENT_RANK() OVER(ORDER BY MPG) AS P_RANK,
       NTILE(5) OVER(ORDER BY MPG) AS N_TILE,
       RATIO_TO_REPORT(MPG) OVER(PARTITION BY CYL) AS R_REPORT
FROM MTCARS
WHERE CYL <= 6;