----단일행 서브쿼리
SELECT EMPNO,
       ENAME,
       SAL
FROM EMP
WHERE SAL >= (SELECT AVG(E.SAL) FROM EMP E);

----다중행 서브쿼리
SELECT EMPNO,
       ENAME,
       SAL,
       DEPTNO
FROM EMP
WHERE DEPTNO IN (SELECT DISTINCT DEPTNO
                 FROM EMP
                 WHERE SAL <= 1000);
---다중컬럼 중첩 서브쿼리 
SELECT EMPNO,
       ENAME,
       JOB,
       SAL
FROM EMP
WHERE (JOB, SAL) IN (SELECT JOB, MAX(SAL)
                     FROM EMP
                     GROUP BY JOB);

